#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb(void)
{
	int		i[3];

	i[0] = 0;
	while (i[0] <= 9)
	{
		i[1] = i[0] + 1;
		while (i[1] <= 9)
		{
			i[2] = i[1] + 1;
			while (i[2] <= 9)
			{
				ft_putchar(i[0] + 48);
				ft_putchar(i[1] + 48);
				ft_putchar(i[2] + 48);
				i[2]++;
				if (i[0] != 7)
					write(1, ", ", 2);
			}
			i[1]++;
		}
		i[0]++;
	}
}
